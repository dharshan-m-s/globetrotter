const getWeatherIcon = (text: string) => {
    const t = text.toLowerCase();
    if (t.includes('rain') || t.includes('shower') || t.includes('wet') || t.includes('drizzle')) return 'üåßÔ∏è';
    if (t.includes('storm') || t.includes('thunder') || t.includes('lightning')) return '‚õàÔ∏è';
    if (t.includes('snow') || t.includes('ice') || t.includes('cold') || t.includes('freez') || t.includes('winter') || t.includes('frost')) return '‚ùÑÔ∏è';
    if (t.includes('fog') || t.includes('mist') || t.includes('haze')) return 'üå´Ô∏è';
    if (t.includes('cloud') || t.includes('overcast') || t.includes('gray') || t.includes('gloom')) return '‚òÅÔ∏è';
    if (t.includes('sun') || t.includes('clear') || t.includes('bright') || t.includes('hot') || t.includes('dry') || t.includes('sunny')) return '‚òÄÔ∏è';
    if (t.includes('wind') || t.includes('breez')) return 'üí®';
    return 'üå§Ô∏è';
};

const getBookingUrl = (platform: string, hotelName: string, destinationName: string) => {
    const query = encodeURIComponent(`${hotelName} ${destinationName}`);
    const p = platform.toLowerCase();
    if (p.includes('booking')) return `https://www.booking.com/searchresults.html?ss=${query}`;
    if (p.includes('expedia')) return `https://www.expedia.com/Hotel-Search?destination=${encodeURIComponent(destinationName)}&keyword=${encodeURIComponent(hotelName)}`;
    if (p.includes('hotels.com')) return `https://www.hotels.com/search.do?q-destination=${encodeURIComponent(destinationName)}&q-hotel=${encodeURIComponent(hotelName)}`;
    if (p.includes('agoda')) return `https://www.agoda.com/search?text=${query}`;
    if (p.includes('tripadvisor')) return `https://www.tripadvisor.com/Search?q=${query}`;
    return `https://www.google.com/search?q=book+${query}`;
};

